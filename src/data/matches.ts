export function getMatchesByTeam(teamId: string): Match[] {
  return matches.filter((match) => match.teamId === teamId);
}

export function getUpcomingMatches(teamId: string): Match[] {
  return matches
    .filter((match) => match.teamId === teamId && match.status === 'upcoming')
    .sort((a, b) => new Date(a.date).getTime() - new Date(b.date).getTime());
}

export function getRecentMatches(teamId: string): Match[] {
  return matches
    .filter((match) => match.teamId === teamId && match.status === 'finished')
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
}
